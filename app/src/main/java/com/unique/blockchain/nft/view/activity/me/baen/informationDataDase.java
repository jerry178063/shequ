package com.unique.blockchain.nft.view.activity.me.baen;

public class informationDataDase {
     String lian_m;
     String dian_m;
     String di_m;
     String xiang_m;

    public informationDataDase(String lian_m, String dian_m, String di_m, String xiang_m) {
        this.lian_m = lian_m;
        this.dian_m = dian_m;
        this.di_m = di_m;
        this.xiang_m = xiang_m;
    }

    public String getLian_m() {
        return lian_m;
    }

    public void setLian_m(String lian_m) {
        this.lian_m = lian_m;
    }

    public String getDian_m() {
        return dian_m;
    }

    public void setDian_m(String dian_m) {
        this.dian_m = dian_m;
    }

    public String getDi_m() {
        return di_m;
    }

    public void setDi_m(String di_m) {
        this.di_m = di_m;
    }

    public String getXiang_m() {
        return xiang_m;
    }

    public void setXiang_m(String xiang_m) {
        this.xiang_m = xiang_m;
    }
}
